    public void load_table_riwayat() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        model.addColumn("ID");
        model.addColumn("Tgl sewa");
        model.addColumn("Tgl Kembali");
        model.addColumn("Nama penyewa");
        model.addColumn("Barang sewaan");
        model.addColumn("Jumlah");
        model.addColumn("Harga satuan");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        model.addColumn("Total biaya");
        model.addColumn("Jumlah bayar");
        model.addColumn("Kembalian/Kurang");
        //menampilkan data database kedalam tabel
        try {
            String sql;
            if (!"".equals(searchRiwayat.getText()) && searchRiwayat.isFocusOwner()) {
                sql = "select * from pembayaran where tgl_sewa like '%" + searchRiwayat.getText() + "%'"
                        + "or tgl_kembali like '%" + searchRiwayat.getText() + "%' "
                        + "or nama_penyewa like '%" + searchRiwayat.getText() + "%' "
                        + "or barang_sewaan like '%" + searchRiwayat.getText() + "%' "
                        + "or jumlah like '%" + searchRiwayat.getText() + "%' "
                        + "or harga_satuan like '%" + searchRiwayat.getText() + "%' "
                        + "or alamat like '%" + searchRiwayat.getText() + "%' "
                        + "or telp like '%" + searchRiwayat.getText() + "%' "
                        + "or total_biaya like '%" + searchRiwayat.getText() + "%' "
                        + "or jumlah_bayar like '%" + searchRiwayat.getText() + "%' "
                        + "or sisa like '%" + searchRiwayat.getText() + "%'";
            } else {
                if (belumLunasCheckbox.isSelected()) {
                    sql = "select * from pembayaran where sisa like '-%'";
                } else {
                    sql = "select * from pembayaran";
                }

            }
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    formatter.format(res.getDate(2)),
                    formatter.format(res.getDate(3)),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7),
                    res.getString(8),
                    res.getString(9),
                    res.getString(10),
                    res.getString(11),
                    res.getString(12)
                });
            }
            tabelRiwayat.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }

