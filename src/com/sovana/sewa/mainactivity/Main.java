    public void showSummaryCount() {
        numberBarang.setText("...");
        numberSewa.setText("...");
        numberBlmLunas.setText("...");
        java.awt.EventQueue.invokeLater(() -> {
            try {
                Connection conn = (Connection) Connect.configDB();
                java.sql.Statement stm = conn.createStatement();
                ResultSet countSewa = stm.executeQuery("select count(id) from sewaan");
                while (countSewa.next()) {
                    numberSewa.setText(countSewa.getString(1));
                }
                ResultSet countBarang = stm.executeQuery("select count(id) from barang");
                while (countBarang.next()) {
                    numberBarang.setText(countBarang.getString(1));
                }
                ResultSet countBlmLunas = stm.executeQuery("select count(id) from pembayaran where sisa like '-%'");
                while (countBlmLunas.next()) {
                    numberBlmLunas.setText(countBlmLunas.getString(1));
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
            }
        });
    }

    public void load_table_sewaan() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        model.addColumn("ID");
        model.addColumn("Tanggal sewa");
        model.addColumn("Nama penyewa");
        model.addColumn("Barang sewaan");
        model.addColumn("Jumlah");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        //menampilkan data database kedalam tabel
        try {
            String sql;
            if (!searchSewa.getText().isEmpty()) {
                sql = "select * from sewaan where tanggal like '%" + searchSewa.getText() + "%'"
                        + "or nama like '%" + searchSewa.getText() + "%' "
                        + "or barang_sewaan like '%" + searchSewa.getText() + "%' "
                        + "or jumlah like '%" + searchSewa.getText() + "%' "
                        + "or alamat like '%" + searchSewa.getText() + "%' "
                        + "or telp like '%" + searchSewa.getText() + "%'";
            } else {
                sql = "select*from sewaan";
            }
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    formatter.format(res.getDate(2)),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7)});
            }
            tabelSewaan.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }

