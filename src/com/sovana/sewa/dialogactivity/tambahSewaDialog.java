    private void populateBarangItem() {
        try {
            String sql = "select nama_barang from barang";
            Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                Object[] o = new Object[1];
                o[0] = res.getString(1);
                barangTextSewa.addItem((String) o[0]);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }

    public void lastIdSewaan() {
        try {
            String sql = "select max(id) from sewaan";
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                int nomer = res.getInt("max(id)") + 1;
                idTextSewa.setText(Integer.toString(nomer));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }

    public void addMode() {
        setTitle("Tambah sewaan baru");
        jLabel10.setText("Tambah sewaan baru");
        btnTambah.setText("Tambah");
        populateBarangItem();
        lastIdSewaan();
    }

    public void editMode(String id_number) {
        setTitle("Edit sewaan");
        jLabel10.setText("Edit sewaan");
        btnTambah.setText("Simpan");
        populateBarangItem();
        try {
            String sql = "select*from sewaan where id = " + id_number + "";
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                idTextSewa.setText(res.getString(1));
                tglSewa.setDate(res.getDate(2).toLocalDate());
                namaTextSewa.setText(res.getString(3));
                barangTextSewa.setSelectedItem(res.getString(4));
                jumlahBarangSewa.setValue(Integer.parseInt(res.getString(5)));
                alamatTextSewa.setText(res.getString(6));
                telpTextSewa.setText(res.getString(7));
            }
        } catch (SQLException e) {
            e.getMessage();
        }

    }

