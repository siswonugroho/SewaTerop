    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (jLabel10.getText().equals("Tambah sewaan baru")) {
            try {
                String sql = "INSERT INTO sewaan (tanggal,nama,barang_sewaan,jumlah,alamat,telp) VALUES "
                        + "('" + tglSewa.getDateStringOrEmptyString()
                        + "','" + namaTextSewa.getText()
                        + "','" + barangTextSewa.getSelectedItem().toString()
                        + "','" + jumlahBarangSewa.getValue().toString()
                        + "','" + alamatTextSewa.getText()
                        + "','" + telpTextSewa.getText() + "')";
                Connection conn = (Connection) Connect.configDB();
                PreparedStatement pst = conn.prepareStatement(sql);
                conn.prepareStatement("ALTER TABLE sewaan AUTO_INCREMENT=0").execute();
                pst.execute();
                ((Main) getOwner()).load_table_sewaan();
                ((Main) getOwner()).showSummaryCount();
                dispose();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan:\n" + e.getMessage(), "Error", 0);
            }

        } else if (jLabel10.getText().equals("Edit sewaan")) {
            try {
                String sql = "UPDATE sewaan SET tanggal = '" + tglSewa.getDateStringOrEmptyString() + "', "
                        + "nama = '" + namaTextSewa.getText() + "', "
                        + "barang_sewaan = '" + barangTextSewa.getSelectedItem().toString() + "', "
                        + "jumlah = '" + jumlahBarangSewa.getValue().toString() + "',"
                        + "alamat = '" + alamatTextSewa.getText() + "',"
                        + "telp= '" + telpTextSewa.getText() + "'"
                        + " WHERE id = '" + idTextSewa.getText() + "'";
                Connection conn = (Connection) Connect.configDB();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                ((Main) getOwner()).load_table_sewaan();
                ((Main) getOwner()).showSummaryCount();
                dispose();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal diedit:\n" + e.getMessage(), "Error", 0);
            }

        }
    }//GEN-LAST:event_btnTambahActionPerformed

