    public void lastIdBarang() {
        try {
            String sql = "select max(id) from barang";
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                int nomer = res.getInt("max(id)") + 1;
                idTextBarang.setText(Integer.toString(nomer));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }

    public void addMode() {
        setTitle("Tambah barang baru");
        jLabel10.setText("Tambah barang baru");
        btnTambah.setText("Tambah");
        populateStatusItem();
        lastIdBarang();
    }

    public void editMode(String id_number) {
        setTitle("Edit barang");
        jLabel10.setText("Edit barang");
        btnTambah.setText("Simpan");
        populateStatusItem();
        try {
            String sql = "select*from barang where id = " + id_number + "";
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                idTextBarang.setText(res.getString(1));
                namaTextBarang.setText(res.getString(2));
                hargaText.setValue(Integer.parseInt(res.getString(3)));
                statusBarang.setSelectedItem(res.getString(4));
            }
        } catch (SQLException e) {
            e.getMessage();
        }

    }

