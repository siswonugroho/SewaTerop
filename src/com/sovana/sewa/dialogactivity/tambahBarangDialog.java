    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (jLabel10.getText().equals("Tambah barang baru")) {
            try {
                String sql = "INSERT INTO barang (nama_barang, harga, status) VALUES "
                        + "('" + namaTextBarang.getText() + "','"
                        + hargaText.getValue().toString() + "','"
                        + statusBarang.getSelectedItem().toString() + "')";
                Connection conn = (Connection) Connect.configDB();
                PreparedStatement pst = conn.prepareStatement(sql);
                conn.prepareStatement("ALTER TABLE barang AUTO_INCREMENT=0").execute();
                pst.execute();
                ((Main) getOwner()).load_table_barang();
                ((Main) getOwner()).showSummaryCount();
                dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan:\n" + e.getMessage(), "Error", 0);
            }

        } else if (jLabel10.getText().equals("Edit barang")) {
            try {
                String sql = "UPDATE barang SET nama_barang = '" + namaTextBarang.getText() + "',"
                        + " harga = '" + hargaText.getValue().toString() + "',"
                        + " status = '" + statusBarang.getSelectedItem().toString() + "'"
                        + " WHERE id = '" + idTextBarang.getText() + "'";
                Connection conn = (Connection) Connect.configDB();
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                ((Main) getOwner()).load_table_barang();
                ((Main) getOwner()).showSummaryCount();
                dispose();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal diedit:\n" + e.getMessage(), "Error", 0);
            }

        }
    }//GEN-LAST:event_btnTambahActionPerformed

