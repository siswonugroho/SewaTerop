    private void btnSetAccessLevelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetAccessLevelActionPerformed
        // TODO add your handling code here:
        try {
            String sql = String.format("update admin set accesslevel='%s' where username='%s'", dropdownHakAkses.getSelectedItem().toString(), namaAdminText.getText());
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            load_table_admin();
            resetform();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengubah hak akses:\n" + e.getMessage(), "Error", 0);
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Pilih satu data terlebih dahulu", "Error", 0);
        }
    }//GEN-LAST:event_btnSetAccessLevelActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            String sql = String.format("delete from admin where username = '%s'", tabelAdmin.getValueAt(tabelAdmin.getSelectedRow(), 0));
            Connection conn = (Connection) Connect.configDB();
            PreparedStatement ps = conn.prepareStatement(sql);
            Object[] tombol = {"Hapus", "Batal"};
            int yatidak = JOptionPane.showOptionDialog(null, String.format("Admin '%s' akan dihapus dan tidak dapat digunakan untuk login kembali.%nLanjutkan?", namaAdminText.getText()), "Konfirmasi hapus",
                    0, 2, null, tombol, tombol[1]);
            if (yatidak == JOptionPane.NO_OPTION) {
                this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            } else if (yatidak == JOptionPane.YES_OPTION) {
                ps.execute();
                load_table_admin();
                resetform();
            }

        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Pilih satu data terlebih dahulu", "Error", 0);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data:\n" + e.getMessage(), "Error", 0);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

