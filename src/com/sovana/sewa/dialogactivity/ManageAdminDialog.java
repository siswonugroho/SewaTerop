    private void selectdata(){
        int baris = tabelAdmin.getSelectedRow();
        namaAdminText.setText(tabelAdmin.getValueAt(baris, 0).toString());
        dropdownHakAkses.setSelectedItem(tabelAdmin.getValueAt(baris, 1));
    }
    
    private void resetform(){
        namaAdminText.setText("Pilih dari tabel...");
        dropdownHakAkses.setSelectedIndex(0);
    }
    
    private void load_table_admin() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        model.addColumn("USERNAME");
        model.addColumn("HAK AKSES");
        //menampilkan data database kedalam tabel
        try {
            String sql = "SELECT username, accesslevel FROM `admin` WHERE accesslevel NOT IN ('owner')";
            java.sql.Connection conn = (Connection) Connect.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2)
                });
            }
            tabelAdmin.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", 0);
        }
    }
    
